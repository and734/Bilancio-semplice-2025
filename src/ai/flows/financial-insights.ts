'use server';
/**
 * @fileOverview AI-powered financial insights from balance sheet data.
 *
 * - getFinancialInsights - A function that generates personalized financial insights and recommendations.
 * - FinancialInsightsInput - The input type for the getFinancialInsights function.
 * - FinancialInsightsOutput - The return type for the getFinancialInsights function.
 */

import {ai} from '@/ai/genkit';
import {z} from 'genkit';

const FinancialInsightsInputSchema = z.object({
  balanceSheetData: z
    .string()
    .describe(
      'The balance sheet data in PDF, XLS, or XML format.  This should be a string representation of the data.'
    ),
});
export type FinancialInsightsInput = z.infer<typeof FinancialInsightsInputSchema>;

const FinancialInsightsOutputSchema = z.object({
  insights: z.string().describe('Personalized insights and recommendations based on the balance sheet data.'),
});
export type FinancialInsightsOutput = z.infer<typeof FinancialInsightsOutputSchema>;

export async function getFinancialInsights(input: FinancialInsightsInput): Promise<FinancialInsightsOutput> {
  return financialInsightsFlow(input);
}

const prompt = ai.definePrompt({
  name: 'financialInsightsPrompt',
  input: {schema: FinancialInsightsInputSchema},
  output: {schema: FinancialInsightsOutputSchema},
  prompt: `You are a financial expert providing insights and recommendations based on balance sheet data.

  Analyze the following balance sheet data and provide personalized insights and recommendations.

  Balance Sheet Data:
  {{balanceSheetData}}
  `,
});

const financialInsightsFlow = ai.defineFlow(
  {
    name: 'financialInsightsFlow',
    inputSchema: FinancialInsightsInputSchema,
    outputSchema: FinancialInsightsOutputSchema,
  },
  async input => {
    const {output} = await prompt(input);
    return output!;
  }
);
